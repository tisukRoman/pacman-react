export const game = (state = gameState, action: ActionDir): GameState => {
  switch (action.type) {
    case c.CHANGE_PACMAN_DIRECTION:
      return {
        ...state,
        pacman: pacman(state.pacman, action),
      };
    default:
      return state;
  }
};

export const pacman = (state = gameState.pacman, action: ActionDir) => {
  switch (action.type) {
    case c.CHANGE_PACMAN_DIRECTION:
      return {
        coords: state.coords,
        direction: action.direction,
      };
    default:
      return state;
  }
};

// Additional functions

function findPacmanCoords(scheme: number[][]): [number, number] {
  for (let i = 0; i < 21; i++) {
    for (let j = 0; j < 25; j++) {
      if (scheme[i][j] === 3) return [i, j];
    }
  }
  return [-1, -1]; // pacman was not found
}

